package by.epamtc.homework.array.onedimentional.task2;

public class MinElementCountModel {
    private int minElement;
    private int countOfMinElement;

    public MinElementCountModel(int minElement, int countOfMinElement) {
        this.minElement = minElement;
        this.countOfMinElement = countOfMinElement;
    }

    public int getMinElement() {
        return minElement;
    }

    public int getCountOfMinElement() {
        return countOfMinElement;
    }
}
